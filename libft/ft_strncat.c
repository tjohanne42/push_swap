#include <string.h>

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	int		i;
	size_t	y;

	y = 0;
	i = 0;
	while (dest[i] != '\0')
		i++;
	while (y < n && src[y] != '\0')
	{
		dest[i] = src[y];
		i++;
		y++;
	}
	dest[i] = '\0';
	return (dest);
}
