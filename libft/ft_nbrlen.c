#include "libft.h"

size_t		ft_nbrlen(intmax_t nb)
{
	size_t	i;

	i = 0;
	if (nb < 0)
	{
		i++;
		nb = nb * -1;
	}
	while (nb >= 10)
	{
		i++;
		nb = nb / 10;
	}
	return (i + 1);
}
